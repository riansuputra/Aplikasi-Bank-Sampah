    public float total_sampah() {
        int jumlah_baris = 0;
        try {
            String sql = "SELECT SUM(stok) AS jumlah FROM tb_sampah";
            Statement st = Koneksi.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                jumlah_baris = rs.getInt("jumlah");
            }
            
        } catch(Exception e) {
            System.out.println(e);
        }
        return jumlah_baris;
    }
    
